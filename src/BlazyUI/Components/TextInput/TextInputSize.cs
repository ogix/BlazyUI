namespace BlazyUI;

/// <summary>
/// Size variants for the TextInput component.
/// </summary>
public enum TextInputSize
{
    /// <summary>
    /// Default size (medium, no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (input-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (input-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (input-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (input-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (input-xl).
    /// </summary>
    ExtraLarge
}
