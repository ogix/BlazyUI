namespace BlazyUI;

/// <summary>
/// Position variants for the Tooltip component.
/// </summary>
public enum BlazyTooltipPosition
{
    /// <summary>Default position (top).</summary>
    Default,
    /// <summary>Tooltip appears above the element.</summary>
    Top,
    /// <summary>Tooltip appears below the element.</summary>
    Bottom,
    /// <summary>Tooltip appears to the left of the element.</summary>
    Left,
    /// <summary>Tooltip appears to the right of the element.</summary>
    Right
}
