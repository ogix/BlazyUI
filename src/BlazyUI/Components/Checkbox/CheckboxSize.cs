namespace BlazyUI;

/// <summary>
/// Size variants for the Checkbox component.
/// </summary>
public enum CheckboxSize
{
    /// <summary>
    /// Default size (medium, no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (checkbox-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (checkbox-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (checkbox-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (checkbox-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (checkbox-xl).
    /// </summary>
    ExtraLarge
}
