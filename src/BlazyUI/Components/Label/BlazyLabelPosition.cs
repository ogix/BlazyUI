/// Specifies the position of the label text relative to the wrapped content.
/// Only applies when Variant is Input, Select, or Floating.
/// </summary>
public enum BlazyLabelPosition
{
    /// <summary>
    /// Label appears before (left of) the content.
    /// </summary>
    Left,

    /// <summary>
    /// Label appears after (right of) the content.
    /// </summary>
    Right
}
