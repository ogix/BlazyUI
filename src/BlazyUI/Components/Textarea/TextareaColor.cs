    /// Secondary color (textarea-secondary).
    /// </summary>
    Secondary,

    /// <summary>
    /// Accent color (textarea-accent).
    /// </summary>
    Accent,

    /// <summary>
    /// Info color (textarea-info).
    /// </summary>
    Info,

    /// <summary>
    /// Success color (textarea-success).
    /// </summary>
    Success,

    /// <summary>
    /// Warning color (textarea-warning).
    /// </summary>
    Warning,

    /// <summary>
    /// Error color (textarea-error).
    /// </summary>
    Error
