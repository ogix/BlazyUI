namespace BlazyUI;

/// <summary>
/// Size variants for the Textarea component.
/// </summary>
public enum TextareaSize
{
    /// <summary>
    /// Default size (medium, no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (textarea-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (textarea-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (textarea-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (textarea-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (textarea-xl).
    /// </summary>
    ExtraLarge
}
