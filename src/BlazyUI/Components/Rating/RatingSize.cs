namespace BlazyUI;

/// <summary>
/// Size variants for the Rating component.
/// </summary>
public enum RatingSize
{
    /// <summary>
    /// Default size (no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (rating-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (rating-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (rating-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (rating-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (rating-xl).
    /// </summary>
    ExtraLarge
}
