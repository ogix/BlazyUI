namespace BlazyUI;

/// <summary>
/// Size variants for the Range component.
/// </summary>
public enum RangeSize
{
    /// <summary>
    /// Default size (no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (range-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (range-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (range-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (range-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (range-xl).
    /// </summary>
    ExtraLarge
}
