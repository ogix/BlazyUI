namespace BlazyUI;

/// <summary>
/// Color variants for the Status component.
/// </summary>
public enum BlazyStatusColor
{
    /// <summary>No color modifier (default styling).</summary>
    Default,
    /// <summary>Neutral color (status-neutral).</summary>
    Neutral,
    /// <summary>Primary theme color (status-primary).</summary>
    Primary,
    /// <summary>Secondary theme color (status-secondary).</summary>
    Secondary,
    /// <summary>Accent theme color (status-accent).</summary>
    Accent,
    /// <summary>Info color, typically blue (status-info).</summary>
    Info,
    /// <summary>Success color, typically green (status-success).</summary>
    Success,
    /// <summary>Warning color, typically yellow/orange (status-warning).</summary>
    Warning,
    /// <summary>Error color, typically red (status-error).</summary>
    Error
}
