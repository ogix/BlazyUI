namespace BlazyUI;

/// <summary>
/// Style variants for the Button component.
/// </summary>
public enum ButtonStyle
{
    /// <summary>Solid fill (default).</summary>
    Default,
    /// <summary>Outline style with transparent background.</summary>
    Outline,
    /// <summary>Dashed border style.</summary>
    Dash,
    /// <summary>Soft style with muted background.</summary>
    Soft,
    /// <summary>Ghost style with no background.</summary>
    Ghost,
    /// <summary>Link style that looks like a hyperlink.</summary>
    Link
}
