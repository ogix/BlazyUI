namespace BlazyUI;

/// <summary>
/// Size variants for the Radio component.
/// </summary>
public enum RadioSize
{
    /// <summary>
    /// Default size (no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (radio-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (radio-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (radio-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (radio-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (radio-xl).
    /// </summary>
    ExtraLarge
}
