    /// Primary color (radio-primary).
    /// </summary>
    Primary,

    /// <summary>
    /// Secondary color (radio-secondary).
    /// </summary>
    Secondary,

    /// <summary>
    /// Accent color (radio-accent).
    /// </summary>
    Accent,

    /// <summary>
    /// Info color (radio-info).
    /// </summary>
    Info,

    /// <summary>
    /// Success color (radio-success).
    /// </summary>
    Success,

    /// <summary>
    /// Warning color (radio-warning).
    /// </summary>
    Warning,

    /// <summary>
    /// Error color (radio-error).
    /// </summary>
    Error
}
