namespace BlazyUI;

/// <summary>
/// Size variants for the FileInput component.
/// </summary>
public enum FileInputSize
{
    /// <summary>
    /// Default size (no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (file-input-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (file-input-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (file-input-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (file-input-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (file-input-xl).
    /// </summary>
    ExtraLarge
}
