namespace BlazyUI;

/// <summary>
/// Size variants for the Select component.
/// </summary>
public enum SelectSize
{
    /// <summary>
    /// Default size (medium, no modifier class).
    /// </summary>
    Default,

    /// <summary>
    /// Extra small size (select-xs).
    /// </summary>
    ExtraSmall,

    /// <summary>
    /// Small size (select-sm).
    /// </summary>
    Small,

    /// <summary>
    /// Medium size (select-md).
    /// </summary>
    Medium,

    /// <summary>
    /// Large size (select-lg).
    /// </summary>
    Large,

    /// <summary>
    /// Extra large size (select-xl).
    /// </summary>
    ExtraLarge
}
